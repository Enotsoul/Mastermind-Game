package mastermind.data;

import java.io.Serializable;

/**
 *
 * @author lostone
 */
public class GameResult implements Serializable {
    // GameResult class handles the white & black positions for the last Game Move
    private String playerMove, blackWhitePions;

    public GameResult(String playerMove, String blackWhitePions) {
        this.playerMove = playerMove;
        this.blackWhitePions = blackWhitePions;
    }

    public String getBlackWhitePions() {
        return blackWhitePions;
    }

    public void setBlackWhitePions(String blackWhitePions) {
        this.blackWhitePions = blackWhitePions;
    }

    public String getPlayerMove() {
        return playerMove;
    }

    public void setPlayerMove(String playerMove) {
        this.playerMove = playerMove;
    }


}
